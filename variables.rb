# Dynamically typed
x = 5
y = '5'
# Inferred at runtime as types Strings, Integers/Floats, Symbols, Booleans...

# Strings can be written in single/double quotes
# Double quotes for interpolations
sum = 5
interpolated_string = "The total is #{5}."
puts interpolated_string

# Integers for whole numbers, floats for numbers with decimal values
i = 5
f = 4.321

# Symbols are string-like, but immutable
# Used as identifers for machines 
# Denoted by a colon attached to a string
:first_name

# Constants declared by capital letters, seperated by underscores
PI_CONSTANT = 3.14


